// Include headers
#include <stdio.h>

// Entry point to the program
int main() {

    // Variable declaration
    int i, j, k;

    // Execute functions, do math, etc.
    i = 10;
    j = 15;
    k = i * j;

    // Output result
    printf("%d * %d = %d\n", i, j, k); // prints: 10 * 15 = 150

    // Exit code
    return 0;
}